package com.class2;

public class StringExample {

	public static void main(String[] args) {
		
		// string is a class that represents sequence of characters enclosed within double  quotes""
		
		String name="John";
		String address= "123 test Dr";
		String salary= "$150,000 US Dollars";
		String phoneNumber= "1234567890";
		
		
		System.out.println(name);// John
		System.out.println(address);// 123 test Dr
		System.out.println(salary);//$150,000 US Dollars
		System.out.println(phoneNumber);//1234567890
		
		
		
	}
}
